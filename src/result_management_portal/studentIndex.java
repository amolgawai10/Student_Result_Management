    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollno = jTextField4.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver"); //calling driver of sql class 
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","Ravanishere@10");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student inner join result on student.rollno = '"+rollno+"' and result.rollno='"+rollno+"'");
            if(rs.next()){
                jTextField1.setText(rs.getString(2)); //course on 2nd column
                jTextField2.setText(rs.getString(3)); //branch name on 3column
                jTextField3.setText(rs.getString(4)); //name on 4
                jTextField6.setText(rs.getString(5)); //gender on 5
                jTextField5.setText(rs.getString(6));//fathername on 6
                
                jTextField10.setText(rs.getString(8)); //os on 8
                jTextField11.setText(rs.getString(9)); // cn on 9
                jTextField12.setText(rs.getString(10)); // maths on 10
                jTextField13.setText(rs.getString(11)); // dbms 11
                jTextField14.setText(rs.getString(12)); //oops 12
                
                //calculate total marks
                int s1= Integer.parseInt(jTextField10.getText());//marks of OS
                int s2 = Integer.parseInt(jTextField11.getText());//marks of CN
                int s3 = Integer.parseInt(jTextField12.getText());//marks of maths
                int s4 = Integer.parseInt(jTextField13.getText());//marks of dbms
                int s5 = Integer.parseInt(jTextField14.getText());//marks of oops
                
                if(s1 < 30 || s2 < 30 || s3 < 30 || s4 < 30 || s5 < 30){ //Someone fail to get more than 30 marks in any subject, he is fail 
                    String totalMarks = String.valueOf(s1+s2+s3+s4+s5);
                    jTextField15.setText(totalMarks + "     [ " + "Fail" + " ]");
                }else{
                    String totalMarks = String.valueOf(s1+s2+s3+s4+s5);
                    jTextField15.setText(totalMarks+"     [ "+"Pass"+" ]");
                }
                
            }
            else{
                JOptionPane.showMessageDialog(null,"Incorrect roll number");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_formComponentShown

